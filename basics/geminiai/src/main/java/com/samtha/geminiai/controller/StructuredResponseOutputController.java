package com.samtha.geminiai.controller;

import com.samtha.geminiai.models.CountryCities;
import org.springframework.ai.chat.client.ChatClient;

import org.springframework.ai.chat.client.advisor.SimpleLoggerAdvisor;
import org.springframework.ai.converter.ListOutputConverter;
import org.springframework.ai.converter.MapOutputConverter;
import org.springframework.core.ParameterizedTypeReference;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.RestController;
import org.springframework.http.ResponseEntity;

import java.util.List;
import java.util.Map;

@RestController
@RequestMapping("/api")
public class StructuredResponseOutputController {

    private ChatClient chatClient;

    public StructuredResponseOutputController(ChatClient.Builder chatClientBuilder){
        this.chatClient=chatClientBuilder
                .defaultAdvisors(new SimpleLoggerAdvisor())
                .build();
    }

    @GetMapping("/structure")
    public ResponseEntity<CountryCities> chatBean(@RequestParam("message") String message) {
        CountryCities countryCities = chatClient
                .prompt()
                .user(message)
                .call().entity(CountryCities.class);
        return ResponseEntity.ok(countryCities);
    }

    @GetMapping("/structure-list")
    public ResponseEntity<List<String>> chatBeanList(@RequestParam("message") String message) {
        List<String> cities = chatClient
                .prompt()
                .user(message)
                .call()
                .entity(new ListOutputConverter());
        return ResponseEntity.ok(cities);
    }

    @GetMapping("/structure-map")
    public ResponseEntity<Map<String,Object>> chatBeanMap(@RequestParam("message") String message) {
        Map<String,Object >cities = chatClient
                .prompt()
                .user(message)
                .call()
                .entity(new MapOutputConverter());
        return ResponseEntity.ok(cities);
    }

    @GetMapping("/structure-pojo-list")
    public ResponseEntity<List<CountryCities>> chatBeanPojoList(@RequestParam("message") String message) {
        List<CountryCities> cities = chatClient
                .prompt()
                .user(message)
                .call()
                .entity(new ParameterizedTypeReference<List<CountryCities>>() {
                });
        return ResponseEntity.ok(cities);
    }

}
